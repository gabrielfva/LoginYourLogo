package com.co.newchallenge.models;

public class UserAccount {
    private String firstNamePersonalInfo;
    private String lastNamePersonalInfo;
    private String companyName;
    private String address;
    private String city;
    private String zip;
    private String addInfo;
    private String homePhone;
    private String mobilePhone;
    private String aliasAddress;

    public String getFirstNamePersonalInfo() {
        return firstNamePersonalInfo;
    }

    public String getLastNamePersonalInfo() {
        return lastNamePersonalInfo;
    }

    public String getCompanyName() {
        return companyName;
    }

    public String getAddress() {
        return address;
    }

    public String getCity() {
        return city;
    }

    public String getZip() {
        return zip;
    }

    public String getAddInfo() {
        return addInfo;
    }

    public String getHomePhone() {
        return homePhone;
    }

    public String getMobilePhone() {
        return mobilePhone;
    }

    public String getAliasAddress() {
        return aliasAddress;
    }
}
